
public class Ex_08_float {

	public static void main(String[] args) {
		// 실수형 float : 4 byte
		// 소수점 이하 데이터 저장 가능
		//  표시형식 : 숫자.숫자 + f 또는 F
		float f1 = 3.4f;
		float f2 = 3.2f;
		
		System.out.println("3.4f + 3.1f : " + (f1+f2));
		
		System.out.println("3.4f + 3.1f : " + (3.4f + 3.1f));
		
	}
	
}